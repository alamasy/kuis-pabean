const tentang = document.getElementById("tentang");
const pertanyaan = document.getElementById("pertanyaan");
const btnRender = document.getElementById("btn-render");
const jawaban = document.querySelectorAll("#pilihan");
// const jawaban2 = document.querySelectorAll(".jawaban");

// const urut = 1
const jumlahSoal = content.length;
let urut = 0;

// NILAI
let nilai = 0;
let times = 1;

// render tampilan soal
function renderUI() {
  urut = Math.floor(Math.random() * (jumlahSoal - 0) + 0);

  tentang.textContent = content[urut].tentang;
  pertanyaan.textContent = content[urut].pertanyaan;

  jawaban.forEach((el, i) => {
    el.textContent = content[urut].pilihanJawaban[i];
  });
}

renderUI();

btnRender.addEventListener("click", () => {
  renderUI();

  jawaban.forEach((el) => {
    el.classList.remove("green-text");
    el.classList.remove("red-text");
  });
});

jawaban.forEach((el, i) => {
  el.addEventListener("click", () => {
    times++;

    const userAnswer = el.dataset.jawaban;

    if (userAnswer === content[urut].jawaban) {
      el.classList.add("green-text");
    } else {
      el.classList.add("red-text");
    }
  });
});
